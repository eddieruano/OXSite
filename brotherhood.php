                        $('.active').removeClass("active");

                        $('.Collage').fadeOut(200, 'swing', function() {
                            all_pics.prependTo('.Collage');
                            needed_pics = $('.<?php echo $groups[0];?>').detach();
                            garbage = $(".all").detach();
                            garbage = null; //empty trash
                            needed_pics.appendTo(".Collage");
                            needed_pics = null;

                            $('.Collage').fadeIn(10, 'swing', function() {
                                collage();
                                $('.Collage').collageCaption();
                            });
                        });
                        $("#<?php echo $groups[0];?>").addClass("active");
                    });
                    $("#<?php echo $groups[1];?>").click(function() {
                        $('.active').removeClass("active");

                        $('.Collage').fadeOut(200, 'swing', function() {
                            all_pics.prependTo('.Collage');
                            needed_pics = $('.<?php echo $groups[1];?>').detach();
                            garbage = $(".all").detach();
                            garbage = null; //empty trash
                            needed_pics.appendTo(".Collage");
                            needed_pics = null;

                            $('.Collage').fadeIn(10, 'swing', function() {
                                collage();
                                $('.Collage').collageCaption();
                            });
                        });
                        $("#<?php echo $groups[1];?>").addClass("active");
                    });

                    //ends button
                    $("#showAll").click(function() {
                        $('.active').removeClass("active");
                        $('.Collage').fadeOut(200, 'swing', function() {
                            $('.all').hide();
                            all_pics.prependTo(".Collage");

                            $('.Collage').fadeIn(10, 'swing', function() {
                                collage();
                            });
                        });
                        $("#showAll").addClass("active");
                    });


